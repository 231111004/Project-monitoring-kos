    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
  try {
        // Connection to the database
        Connection conn = kosconnector.configDB();
        
        // Counting the number of tenants for each room
        String sqlCount = "SELECT nokamar, COUNT(*) AS jumlah FROM penghuni GROUP BY nokamar";
        PreparedStatement pstmtCount = conn.prepareStatement(sqlCount);
        ResultSet rsCount = pstmtCount.executeQuery();
        
        // Update room status to "sudah berpenghuni" based on the number of tenants
        while (rsCount.next()) {
            String nokamar = rsCount.getString("nokamar");
            int jumlahPenghuni = rsCount.getInt("jumlah");
            
            String status = (jumlahPenghuni > 0) ? "sudah berpenghuni" : "belum berpenghuni";
            
            String sqlUpdate = "UPDATE kamar SET status = ? WHERE nokamar = ?";
            PreparedStatement pstmtUpdate = conn.prepareStatement(sqlUpdate);
            pstmtUpdate.setString(1, status);
            pstmtUpdate.setString(2, nokamar);
            
            pstmtUpdate.executeUpdate();
            pstmtUpdate.close();
            
            // Debug logging
            System.out.println("Updated status kamar for nokamar " + nokamar + " to " + status);
        }
        
        rsCount.close();
        pstmtCount.close();

        // Set rooms with no tenants to "belum berpenghuni"
        String sqlNoTenants = "UPDATE kamar k LEFT JOIN penghuni p ON k.nokamar = p.nokamar SET k.status = 'belum berpenghuni' WHERE p.nokamar IS NULL";
        PreparedStatement pstmtNoTenants = conn.prepareStatement(sqlNoTenants);
        pstmtNoTenants.executeUpdate();
        pstmtNoTenants.close();

        // Reload the table data after updating the status
        newload_table();
        
        conn.close(); // Close the connection after finishing
        
        // Switch to the "kamar" panel
        CardLayout cLayout = (CardLayout) panelutama.getLayout();
        cLayout.show(panelutama, "kamar");

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error updating status kamar: " + e.getMessage());
    }

    }//GEN-LAST:event_jMenuItem3ActionPerformed

