private void load_table() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No");
    model.addColumn("KTP");
    model.addColumn("Nama");
    model.addColumn("No HP");
    model.addColumn("No Kamar");
    model.addColumn("tanggal masuk");
    model.addColumn("tanggal keluar");
    model.addColumn("Tagihan");

    try {
        int no = 1;
        String sql = "SELECT p.*, k.harga FROM penghuni p LEFT JOIN kamar k ON p.nokamar = k.nokamar";
        Connection conn = kosconnector.configDB();
        Statement stm = conn.createStatement();
        ResultSet res = stm.executeQuery(sql);

        while (res.next()) {
            String tglmasuk = res.getString("tglmasuk");
            String tglkeluar = res.getString("tglkeluar");
            int harga = res.getInt("harga");

           
            java.time.LocalDate startDate = java.time.LocalDate.parse(tglmasuk);
            java.time.LocalDate endDate = java.time.LocalDate.parse(tglkeluar);
            java.time.Period period = java.time.Period.between(startDate, endDate);
            int months = period.getMonths() + period.getYears() * 12 + 1; // tambah 1 agar bulan masuk dan keluar termasuk

            int tagihan = harga * months;

            model.addRow(new Object[]{
                no++,
                res.getString("KTP"),
                res.getString("nama"),
                res.getString("telp"),
                res.getString("nokamar"),
                res.getString("tglmasuk"),
                res.getString("tglkeluar"),
                tagihan
            });
        }
        jTable1.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error loading data: " + e.getMessage());
    }
}
